SELECT 
    ID_EXAME,
    DATA_CADASTRO,
    COD_REQUISICAO,
    DATA_EXAME,
    DATA_INICIO_EXAME,
    DIA,
    MES,
    ANO,
    HORARIO,
    'TEXTO_SUPRIMIDO' AS HISTORICO,
    'TEXTO_SUPRIMIDO' AS CONCLUSAO,
    'TEXTO_SUPRIMIDO' AS DISCUSSAO,
    NUMERO_PROTOCOLO,
    TIPO_EXAME,
    'TEXTO_SUPRIMIDO' AS EXAME_MEDICO,
    'TEXTO_SUPRIMIDO' AS ODONTO_LEGAL,
    'PACIENTE_SUPRIMIDO' AS PACIENTE,
    TRUNC(DATA_NASCIMENTO, 'YEAR') AS DATA_NASCIMENTO,
    'PAI_SUPRIMIDO' AS PAI,
    'MAE_SUPRIMIDA' AS MAE,
    'DENTISTA_SUPRIMIDO' AS DENTISTA,
    'CRM_SUPRIMIDO' AS CRM,
    'CRO_SUPRIMIDO' AS CRO,
    ESTADO_CIVIL,
    NATURALIDADE,
    MUNICIPIO,
    'PERITO_SUPRIMIDO' AS PERITO_LEGISTA,
    ORGAO_DESTINO,
    'ENDERECO_SUPRIMIDO' AS ENDERECO_PACIENTE,
    NIC
FROM IML.VIEW_NEAC_LAUDOS_IML
WHERE DATA_INICIO_EXAME >= TO_DATE('2025-01-01', 'YYYY-MM-DD')
  AND DATA_INICIO_EXAME <  TO_DATE('2026-01-01', 'YYYY-MM-DD');
