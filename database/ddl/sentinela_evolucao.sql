-- DDL para Fila de Evolução Pendente e Notificações do IML no SENTINELA
-- Compatível com Oracle e mapeado para SQLite local

CREATE TABLE SENTINELA_EVOLUCAO_PENDENTE (
    ID_EVOLUCAO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_CONTROLE_MORTE NUMBER NOT NULL,
    NIC_IML VARCHAR2(100),
    BO_PC VARCHAR2(100),
    DATA_OBITO TIMESTAMP,
    STATUS VARCHAR2(30) DEFAULT 'Pendente', -- 'Pendente', 'Aprovada', 'Rejeitada'
    MOTIVO VARCHAR2(4000),
    TIPO_EVOLUCAO VARCHAR2(100) DEFAULT 'Tentativa -> Óbito',
    AUTOR_PROPOSTA VARCHAR2(100),
    DT_CRIACAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    DT_ATUALIZACAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE SENTINELA_NOTIFICACAO_IML (
    ID_NOTIFICACAO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NIC VARCHAR2(100) NOT NULL,
    NOME_VITIMA VARCHAR2(255),
    STATUS_IML VARCHAR2(100),
    TIPO_MENSAGEM VARCHAR2(255) NOT NULL,
    DT_EVENTO TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    LIDO NUMBER(1) DEFAULT 0
);

-- Índices recomendados
CREATE INDEX IDX_EVOL_CM ON SENTINELA_EVOLUCAO_PENDENTE(ID_CONTROLE_MORTE);
CREATE INDEX IDX_EVOL_STATUS ON SENTINELA_EVOLUCAO_PENDENTE(STATUS);
CREATE INDEX IDX_NOTIF_NIC ON SENTINELA_NOTIFICACAO_IML(NIC);
CREATE INDEX IDX_NOTIF_LIDO ON SENTINELA_NOTIFICACAO_IML(LIDO);
