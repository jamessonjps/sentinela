-- DDL para Tabelas de Controle do SENTINELA
-- Criadas para suportar a arquitetura dos Agentes de IA

CREATE TABLE SENTINELA_FILA_ALERTAS (
    ID_ALERTA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_CONTROLE_MORTE NUMBER,
    NIC VARCHAR2(100),
    BO_PC VARCHAR2(100),
    CAD VARCHAR2(100),
    STATUS VARCHAR2(50) DEFAULT 'Novo', -- Novo, Em Tratativa, Resolvido
    PRIORIDADE NUMBER CHECK (PRIORIDADE IN (1, 2, 3)),
    TIPO_ALERTA VARCHAR2(255) NOT NULL,
    DT_CRIACAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    DT_ATUALIZACAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE SENTINELA_ATRIBUICAO (
    ID_ATRIBUICAO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ID_ANALISTA VARCHAR2(100) NOT NULL,
    ID_ALERTA NUMBER,
    AISP VARCHAR2(50),
    RISP VARCHAR2(50),
    DATA_ATRIBUICAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_ATRIB_ALERTA FOREIGN KEY (ID_ALERTA) REFERENCES SENTINELA_FILA_ALERTAS(ID_ALERTA)
);

CREATE TABLE SENTINELA_AUDITORIA_ERRO (
    ID_AUDITORIA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MODULO_AGENTE VARCHAR2(100) NOT NULL,
    MENSAGEM_ERRO VARCHAR2(4000),
    STACK_TRACE CLOB,
    DT_ERRO TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE SENTINELA_MASCARAMENTO (
    ID_REGRA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TABELA_ORIGEM VARCHAR2(100) NOT NULL,
    CAMPO_ORIGINAL VARCHAR2(100) NOT NULL,
    REGRA_MASCARAMENTO VARCHAR2(255) NOT NULL,
    NIVEL_ACESSO_REQUERIDO VARCHAR2(50) DEFAULT 'RESTRITO',
    ATIVO NUMBER(1) DEFAULT 1
);

CREATE TABLE SENTINELA_CHECKPOINT (
    ID_CHECKPOINT NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    AGENTE_NOME VARCHAR2(100) NOT NULL,
    ULTIMA_VERIFICACAO TIMESTAMP NOT NULL,
    QTD_REGISTROS_PROCESSADOS NUMBER DEFAULT 0,
    DT_ATUALIZACAO TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
