-- DDL para Tabela de Laudos do IML no SENTINELA
-- Compatível com Oracle e SQLite local

CREATE TABLE SENTINELA_LAUDOS_IML (
    ID_EXAME NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NIC VARCHAR2(100) NOT NULL,
    DATA_CADASTRO VARCHAR2(100),
    COD_REQUISICAO VARCHAR2(100),
    DATA_EXAME VARCHAR2(100),
    CONCLUSAO VARCHAR2(4000),
    NUMERO_PROTOCOLO VARCHAR2(100),
    TIPO_EXAME VARCHAR2(100),
    PACIENTE VARCHAR2(255),
    DATA_NASCIMENTO VARCHAR2(100),
    PAI VARCHAR2(255),
    MAE VARCHAR2(255),
    PERITO_LEGISTA VARCHAR2(255),
    ORGAO_DESTINO VARCHAR2(255)
);

-- Índices para cruzamento rápido
CREATE INDEX IDX_LAUDOS_NIC ON SENTINELA_LAUDOS_IML(NIC);
CREATE INDEX IDX_LAUDOS_REQUISICAO ON SENTINELA_LAUDOS_IML(COD_REQUISICAO);
