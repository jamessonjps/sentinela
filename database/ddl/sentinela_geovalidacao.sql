-- DDL para Tabelas de Validação Geográfica no SENTINELA
-- Compatível com Oracle e SQLite local

CREATE TABLE SENTINELA_GEO_BAIRRO (
    ID_BAIRRO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME_BAIRRO VARCHAR2(150) NOT NULL,
    NOME_MUNICIPIO VARCHAR2(150) NOT NULL,
    CENTRO_LATITUDE FLOAT NOT NULL,
    CENTRO_LONGITUDE FLOAT NOT NULL,
    RAIO_KM FLOAT DEFAULT 1.5,
    CONSTRAINT UC_GEO_BAIRRO UNIQUE (NOME_MUNICIPIO, NOME_BAIRRO)
);

CREATE TABLE SENTINELA_ESTABELECIMENTO_SAUDE (
    ID_ESTABELECIMENTO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME VARCHAR2(255) NOT NULL,
    LATITUDE FLOAT NOT NULL,
    LONGITUDE FLOAT NOT NULL,
    RAIO_METROS FLOAT DEFAULT 150.0,
    TIPO VARCHAR2(50) DEFAULT 'SAUDE' -- HOSPITAL, UPA, PRESIDIO
);

-- Índices recomendados
CREATE INDEX IDX_GEO_BAIRRO_BUSCA ON SENTINELA_GEO_BAIRRO(NOME_MUNICIPIO, NOME_BAIRRO);
CREATE INDEX IDX_ESTAB_COORD ON SENTINELA_ESTABELECIMENTO_SAUDE(LATITUDE, LONGITUDE);
